dia = int (input("Digite n° respectivo a um dia da semana: "))

if (dia == 1):
    print(" Dia 1 é Domingo!")

elif (dia == 2):
    print("Dia 2 é Segunda!")
    
elif (dia == 3):
    print("Dia 3 é Terça")
    
elif (dia == 4):
    print("Dia 4 é quarta!")
    
elif (dia == 5):
    print("Dia 5 é quinta!")

elif (dia == 6):
    print("Dia 6 é sexta!")

elif (dia == 7):
    print("Dia 7 é sábado!")

else:
    print("N° não corresponde a nenhum dia da semana!")
    